function calc (a, b, c, d ){

    const soma = a + b + c + d
    const subtracao = a - b - c - d
    const divisao = a / b / c / d
    const multiplicacao = a * b * c *d
    const modulo = a % 2
    return{soma, subtracao, divisao, multiplicacao,modulo}
}

